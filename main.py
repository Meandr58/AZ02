import pandas as pd

# Создаем DataFrame с данными об оценках учеников
data = {
    'Ученик': ['Иванов', 'Петров', 'Сидоров', 'Максимов', 'Борисов',
               'Федоров', 'Кашкина', 'Соколова', 'Кузнецов', 'Светланова'],

    'Математика': [3, 3, 4, 3, 5, 3, 3, 4, 3, 5],
    'Физика': [4, 3, 5, 3, 4, 3, 5, 4, 4, 4],
    'Химия': [3, 4, 4, 2, 4, 4, 3, 3, 3, 4],
    'Биология': [4, 3, 3, 3, 5, 3, 3, 5, 3, 3],
    'История': [5, 3, 5, 4, 5, 4, 4, 5, 3, 3]
}

df = pd.DataFrame(data)

# Вывод первых нескольких строк DataFrame
print("Первые несколько строк DataFrame:")
print(df.head())

# Вычисление средней оценки по каждому предмету
mean_scores = df.mean(numeric_only=True)
print("\nСредняя оценка по каждому предмету:")
print(mean_scores)

# Вычисление медианной оценки по каждому предмету
median_scores = df.median(numeric_only=True)
print("\nМедианная оценка по каждому предмету:")
print(median_scores)

# Вычисление Q1 и Q3 для оценок по математике
Q1_math = df['Математика'].quantile(0.25)
Q3_math = df['Математика'].quantile(0.75)
IQR_math = Q3_math - Q1_math
print("\nQ1 для математики:", Q1_math)
print("Q3 для математики:", Q3_math)
print("IQR для математики:", IQR_math)

# Вычисление стандартного отклонения по каждому предмету
std_deviation = df.std(numeric_only=True)
print("\nСтандартное отклонение по каждому предмету:")
print(std_deviation)
